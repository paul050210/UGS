    private IEnumerable<(string Speaker, string Text)> GetTextInRange(int startIndex, int endIndex)
    {
        for (int i = startIndex; i <= endIndex; i++)
        {
            if (textData.TryGetValue(i, out var value))
            {
                yield return value;
            }
        }
    }

    public IEnumerable<(string Speaker, string Text)> GetTextData(int startIndex, int endIndex)
    {
        // 범위에 따른 데이터 반환
